#include<iostream>
using namespace std;
int main()
{
    int no1,no2;
    cout<<"Enter no 1 :"<<endl;
    cin>>no1;
    cout<<"Enter no 2 :"<<endl;
    cin>>no2;
    cout<<no1<<"+"<<no2<<"="<<no1+no2<<endl;
    cout<<no1<<"-"<<no2<<"="<<no1-no2<<endl;
    cout<<no1<<"*"<<no2<<"="<<no1*no2<<endl;
    cout<<no1<<"/"<<no2<<"="<<no1/no2<<endl;
    cout<<no1<<"%"<<no2<<"="<<no1%no2<<endl;
    return 0;
}